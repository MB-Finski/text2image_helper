<template>
	<div class="text2image-picker-content-wrapper">
		<div v-if="!pickerEnabled" class="text2image-picker-content">
			<h2>
				{{ t('text2image_helper', 'ERROR: No image generation provider available') }}
			</h2>
		</div>
		<div v-if="pickerEnabled" class="text2image-picker-content">
			<h2>
				{{ t('text2image_helper', 'AI Image Generation') }}
			</h2>
			<div class="input-wrapper">
				<NcTextField ref="text2image-search-input"
					:value.sync="query"
					:label="inputPlaceholder"
					:disabled="loading"
					:show-trailing-button="!!query"
					@keydown.enter="generate"
					@trailing-button-click="query = ''" />
			</div>
			<div v-if="result === null || query === ''" class="prompts">
				<NcUserBubble v-for="p in prompts"
					:key="p.id + p.value"
					class="prompt-bubble"
					:title="p.value"
					:size="30"
					avatar-image="icon-history"
					:display-name="p.value"
					@click="query = p.value" />
			</div>
			<div v-if="result !== null" class="preview-container">
				<h3>{{ t('text2image_helper', 'Preview') }}</h3>
				<div class="image-preview">
					<Text2ImageDisplay :key="result.image_gen_id"
						:src="result.url"
						:prompt="result.prompt"
						@failed="imageGenerationFailed"
						@ready="imageGenerationReady" />
				</div>
			</div>
			<div class="footer">
				<div>
					<NcButton class="advanced-button"
						type="tertiary"
						:aria-label="t('text2image_helper', 'Show/hide advanced options')"
						@click="showAdvanced = !showAdvanced">
						<template #icon>
							<component :is="showAdvancedIcon" />
						</template>
						{{ t('text2image_helper', 'Advanced options') }}
					</NcButton>
				</div>
				<div class="buttons">
					<NcButton type="secondary"
						:aria-label="t('text2image_helper', 'Preview image generation by AI')"
						:disabled="loading || !query"
						@click="generate">
						{{ previewButtonLabel }}
						<template #icon>
							<NcLoadingIcon v-if="loading" />
							<RefreshIcon v-else-if="result !== null" />
							<EyeIcon v-else />
						</template>
					</NcButton>
					<NcButton v-if="result !== null"
						type="secondary"
						:disabled="!isGenerating"
						@click="notifyWhenReady">
						{{ t('text2image_helper', 'Notify when ready') }}
						<template #icon>
							<ArrowRightIcon />
						</template>
					</NcButton>
					<NcButton v-if="result !== null"
						type="primary"
						:aria-label="t('text2image_helper', 'Submit image(s) generated by AI')"
						:disabled="loading || emptyResult"
						@click="submit">
						{{ t('text2image_helper', 'Send') }}
						<template #icon>
							<ArrowRightIcon />
						</template>
					</NcButton>
				</div>
			</div>
			<div v-show="showAdvanced" class="advanced">
				<div class="line">
					<NcCheckboxRadioSwitch class="include-query"
						:checked.sync="displayPrompt">
						{{ t('text2image_helper', 'Include the prompt in the result') }}
					</NcCheckboxRadioSwitch>
				</div>
				<div class="spacer" />
				<div class="line">
					<label for="nResults">{{ t('text2image_helper', 'Number of results') }}</label>
					<input id="nResults"
						v-model="nResults"
						type="number"
						min="1"
						max="10">
				</div>
			</div>
		</div>
	</div>
</template>

<script>
import EyeIcon from 'vue-material-design-icons/Eye.vue'
import RefreshIcon from 'vue-material-design-icons/Refresh.vue'
import ArrowRightIcon from 'vue-material-design-icons/ArrowRight.vue'

import NcButton from '@nextcloud/vue/dist/Components/NcButton.js'
import NcLoadingIcon from '@nextcloud/vue/dist/Components/NcLoadingIcon.js'
import NcTextField from '@nextcloud/vue/dist/Components/NcTextField.js'
import NcCheckboxRadioSwitch from '@nextcloud/vue/dist/Components/NcCheckboxRadioSwitch.js'
import NcUserBubble from '@nextcloud/vue/dist/Components/NcUserBubble.js'
import ChevronRightIcon from 'vue-material-design-icons/ChevronRight.vue'
import ChevronDownIcon from 'vue-material-design-icons/ChevronDown.vue'

import axios from '@nextcloud/axios'
import { generateUrl } from '@nextcloud/router'
import { showError, showMessage } from '@nextcloud/dialogs'

import Text2ImageDisplay from '../Components/Text2ImageDisplay.vue'

export default {
	name: 'Text2ImageHelperCustomPickerElement',

	components: {
		NcButton,
		NcLoadingIcon,
		NcTextField,
		NcCheckboxRadioSwitch,
		ArrowRightIcon,
		NcUserBubble,
		RefreshIcon,
		EyeIcon,
		Text2ImageDisplay,
	},

	props: {
		providerId: {
			type: String,
			required: true,
		},
		accessible: {
			type: Boolean,
			default: false,
		},
		pickerEnabled: {
			type: Boolean,
			default: true,
		},
	},

	data() {
		return {
			query: '',
			result: null,
			loading: false,
			isGenerating: false,
			models: [],
			inputPlaceholder: t('text2image_helper', 'A hedgehog laid back on a couch'),
			displayPrompt: true,
			notify: false,
			prompts: null,
			submitted: false,
			nResults: 1,
			showAdvanced: false,
		}
	},

	computed: {
		previewButtonLabel() {
			return this.result !== null
				? t('text2image_helper', 'Regenerate')
				: t('text2image_helper', 'Preview')
		},
		emptyResult() {
			return this.result === null || this.result.length === 0
		},
		showAdvancedIcon() {
			return this.showAdvanced
				? ChevronDownIcon
				: ChevronRightIcon
		},
	},

	watch: {
		displayPrompt(newValue) {
			localStorage.setItem('text2image_display_prompt', JSON.stringify(newValue))
		},
	},

	mounted() {
		this.focusOnInput()
		this.getPromptHistory()
		const cachedValue = localStorage.getItem('text2image_display_prompt') === 'true'
		if (cachedValue !== null) {
			this.displayPrompt = cachedValue
		}
	},

	beforeDestroy() {
		if (!this.submitted && !this.notify) {
			this.cancelGeneration()
		}
	},

	methods: {
		focusOnInput() {
			setTimeout(() => {
				this.$refs['text2image-search-input'].$el.getElementsByTagName('input')[0]?.focus()
			}, 300)
		},
		getPromptHistory() {
			const params = {
				params: {
				},
			}
			const url = generateUrl('/apps/text2image_helper/prompt_history')
			return axios.get(url, params)
				.then((response) => {
					this.prompts = response.data
				})
				.catch((error) => {
					console.error(error)
				})
		},
		submit() {
			this.submitted = true
			this.$emit('submit', this.result.reference_url)
		},
		insertPrompt(prompt) {
			if (this.prompts.find(p => p.value === prompt) === undefined) {
				this.prompts.unshift({
					id: 0,
					value: prompt,
				})
			}
		},
		cancelGeneration() {
			const url = generateUrl('/apps/text2image_helper/cancel_generation')
			axios.post(url, { imageGenId: this.result.image_gen_id })
				.catch((error) => {
					console.error('Image generation cancel request error', error)
					showError(
						t('text2image_helper', 'Image generation cancel error') + ': '
						+ (error.response?.data?.body?.error?.message
							|| error.response?.data?.body?.error?.code
							|| error.response?.data?.error
							|| t('text2image_helper', 'Unknown image generation cancel error')
						),
					)
				})
		},
		generate() {

			if (this.result !== null) {
				this.cancelGeneration()
			}

			if (this.query === '') {
				return
			}

			this.isGenerating = true
			this.loading = true

			const params = {
				prompt: this.query,
				nResults: this.nResults,
				displayPrompt: this.displayPrompt,
			}
			const url = generateUrl('/apps/text2image_helper/process_prompt')
			return axios.post(url, params)
				.then((response) => {
					const data = response.data
					if (data?.url !== undefined) {
						this.result = data
						this.insertPrompt(this.query)
					} else {
						showError(t('text2image_helper', 'Unexpected response from server.'))
					}
				})
				.catch((error) => {
					console.error('Image generation request error', error)
					showError(
						t('text2image_helper', 'Image generation error') + ': '
						+ (error.response?.data?.body?.error?.message
							|| error.response?.data?.body?.error?.code
							|| error.response?.data?.error
							|| t('text2image_helper', 'Unknown image generation error')
						),
					)
				})
				.then(() => {
					this.loading = false
				})
		},
		notifyWhenReady() {
			if (this.failed || this.result === null) {
				return
			}
			const url = generateUrl('/apps/text2image_helper/n/' + this.result.image_gen_id)
			axios.post(url)
				.then(() => {
					showMessage(t('text2image_helper', 'You will be notified when the image generation is ready.'))
					this.notify = true
					this.$emit('submit', '')
				})
				.catch((error) => {
					console.error('Notify when ready request error', error)
					showError(
						t('text2image_helper', 'Notify when ready error') + ': '
						+ (error.response?.data?.body?.error?.message
							|| error.response?.data?.body?.error?.code
							|| error.response?.data?.error
							|| t('text2image_helper', 'Unknown notify when ready error')
						),
					)
				})
		},
		imageGenerationFailed() {
			this.isGenerating = false
		},
		imageGenerationReady() {
			this.isGenerating = false
		},
	},
}
</script>

